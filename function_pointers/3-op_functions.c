/**
 * op_add - to add two integer
 * @a: the first number
 * @b: the second number
 *
 * Return: sum
 */
int op_add(int a, int b)
{
        return (a + b);
}
/**
 * op_sub - to subtract two integer
 * @a: the first number
 * @b: the second number
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
        return (a - b);
}
/**
 * op_mul - to multiply two integer
 * @a: the first number
 * @b: the second number
 *
 * Return: product
 */
int op_mul(int a, int b)
{
        return (a * b);
}
